package com.arraylogic;

import java.util.HashSet;
import java.util.Set;

public class DuplicateArray {


    public static void main(String[] args) {
        int[] arr ={1,1,2,2,3,3};

       Set<Integer> set = new HashSet<>();

       for(int nums : arr){
           set.add(nums);
       }






    }

}
